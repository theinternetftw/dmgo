package dmgo

type textDisplay struct {
	x, y   int
	w, h   int
	screen []byte // w*h*4
}

func (t *textDisplay) newline() {
	t.x = 0
	t.y += 8
	if t.y >= t.h {
		t.y = 0
	}
}

func (t *textDisplay) advanceChar() {
	t.x += 8
	if t.x >= t.w {
		t.newline()
	}
}

func (t *textDisplay) setPos(x, y int) {
	t.x = x * 8
	t.y = y * 8
}

func (t *textDisplay) clearLine() {
	startX, startY := t.x, t.y
	for t.y == startY {
		t.writeChar(' ')
	}
	t.x, t.y = startX, startY
}

func (t *textDisplay) clearScreen() {
	for i := 0; i < len(t.screen); i++ {
		t.screen[i] = 0
	}
}

func (t *textDisplay) writeString(str string) {
	for _, char := range str {
		if char == '\x00' {
			continue
		}
		t.writeChar(char)
	}
}

func (t *textDisplay) writeChar(char rune) {
	if char == '\n' {
		t.newline()
	} else {
		if char == '\t' {
			char = ' '
		}
		if char >= 32 {
			if char >= 'a' && char <= 'z' {
				char = char - 'a' + 'A'
			}
			fontChr, ok := dbgFont[char]
			if !ok {
				fontChr = dbgFont['?']
			}
			for i := 0; i < 7; i++ {
				line := t.screen[(t.y+i)*t.w*4+t.x*4:]
				chrLine := fontChr[i*7 : i*7+7]
				for j := 0; j < 7; j++ {
					col := byte(0)
					if chrLine[j] == 1 {
						col = 0xff
					}
					line[j*4+0], line[j*4+1] = col, col
					line[j*4+2], line[j*4+3] = col, col
				}
			}
			t.advanceChar()
		}
	}
}

